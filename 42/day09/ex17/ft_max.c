int		ft_max(int *tab, int length)
{
	int max;
	int i;

	i = 0;
	if (tab[0])
	{
		max = tab[0];
		while (i < length)
		{
			if (max < tab[i])
			{
				max = tab[i];
			}
			i++;
		}
	}
	return (max);
}
